<?php
// Ορίζουμε σε μεταβλητές τα "κλειδιά" για την πόρτα της βάσης
$servername = "localhost"; // Η διεύθυνση του διακομιστή (συνήθως localhost για τοπική ανάπτυξη)
$username = "root";        // Το όνομα χρήστη για τη βάση δεδομένων (προεπιλογή του XAMPP)
$password = "";            // Ο κωδικός πρόσβασης (στο XAMPP είναι κενός από προεπιλογή)
$dbname = "anime_wallpapers";   // Το όνομα της βάσης δεδομένων που περιέχει τους πίνακες μας

// Δημιουργία σύνδεσης με τη βάση δεδομένων χρησιμοποιώντας τη συνάρτηση mysqli_connect
$conn = mysqli_connect($servername, $username, $password, $dbname);

// Έλεγχος της σύνδεσης: Αν η σύνδεση αποτύχει, η εκτέλεση του κώδικα σταματά
if (!$conn) {
    // Η die() διακόπτει το script και εμφανίζει το μήνυμα σφάλματος της MySQL
    die("Η σύνδεση απέτυχε: " . mysqli_connect_error());
}

// Προαιρετικό: Ορισμός της κωδικοποίησης σε utf8 για σωστή εμφάνιση ελληνικών χαρακτήρων
mysqli_set_charset($conn, "utf8");
?>